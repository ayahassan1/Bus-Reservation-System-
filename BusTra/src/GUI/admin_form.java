    private void tripcodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tripcodeKeyReleased
        String code=tripcode.getText();
        if(code.length()==0&&errorcode.getText().length()==0){
            errorcode.setText("Please enter trip code");
            tripcode.setBorder(BorderFactory.createLineBorder(Color.decode("#FF0000")));
           
        }
        else{
            boolean check= admin.if_tripcode_valid(code);
            if(code.length()==0){
                errorcode.setVisible(true);
                errorcode.setText("Please enter trip code");
                tripcode.setBorder(BorderFactory.createLineBorder(Color.decode("#FF0000")));
            }
            else{
                if(check==true){
                    errorcode.setText("");
                    errorcode.setVisible(false);
                    tripcode.setBorder(BorderFactory.createLineBorder(Color.decode("#4DBD33")));
                }
                else  if(check==false)
                {
                    errorcode.setVisible(true);
                    tripcode.setBorder(BorderFactory.createLineBorder(Color.decode("#FF0000")));
                    errorcode.setText("already exists.");
                }
            }}

    }//GEN-LAST:event_tripcodeKeyReleased

